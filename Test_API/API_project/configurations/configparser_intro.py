# Introduction
# For every developer the day comes, when he or she is working on large and consequently complex projects.
# To maintain the software, configuration files can be very beneficial and time-saving.
#
# Instead of changing the source code itself, “only” configuration files need to be adjusted or exchanged
# to access a different API endpoint, update the base URL, or similar things.

# While there are various ways to support configuration files in your software
# including JSON, YAML, and plain-text files, this article aims to give you an
# introduction to the configparser module from the standard library.